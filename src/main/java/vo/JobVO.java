public class JobVO {
	private Long id;
	private Long client_id;
	private String place;
	private String occupation;
	private String job;
	private String time_working;
	private String phone;
	private String extension;
	private String payroll;
	private Double income;
	/**
	 * @return the id
	 */
	public Long getId() {
		return id;
	}
	/**
	 * @param id the id to set
	 */
	public void setId(Long id) {
		this.id = id;
	}
	/**
	 * @return the client_id
	 */
	public Long getClient_id() {
		return client_id;
	}
	/**
	 * @param client_id the client_id to set
	 */
	public void setClient_id(Long client_id) {
		this.client_id = client_id;
	}
	/**
	 * @return the place
	 */
	public String getPlace() {
		return place;
	}
	/**
	 * @param place the place to set
	 */
	public void setPlace(String place) {
		this.place = place;
	}
	/**
	 * @return the occupation
	 */
	public String getOccupation() {
		return occupation;
	}
	/**
	 * @param occupation the occupation to set
	 */
	public void setOccupation(String occupation) {
		this.occupation = occupation;
	}
	/**
	 * @return the job
	 */
	public String getJob() {
		return job;
	}
	/**
	 * @param job the job to set
	 */
	public void setJob(String job) {
		this.job = job;
	}
	/**
	 * @return the time_working
	 */
	public String getTime_working() {
		return time_working;
	}
	/**
	 * @param time_working the time_working to set
	 */
	public void setTime_working(String time_working) {
		this.time_working = time_working;
	}
	/**
	 * @return the phone
	 */
	public String getPhone() {
		return phone;
	}
	/**
	 * @param phone the phone to set
	 */
	public void setPhone(String phone) {
		this.phone = phone;
	}
	/**
	 * @return the extension
	 */
	public String getExtension() {
		return extension;
	}
	/**
	 * @param extension the extension to set
	 */
	public void setExtension(String extension) {
		this.extension = extension;
	}
	/**
	 * @return the payroll
	 */
	public String getPayroll() {
		return payroll;
	}
	/**
	 * @param payroll the payroll to set
	 */
	public void setPayroll(String payroll) {
		this.payroll = payroll;
	}
	/**
	 * @return the income
	 */
	public Double getIncome() {
		return income;
	}
	/**
	 * @param income the income to set
	 */
	public void setIncome(Double income) {
		this.income = income;
	}
	

}
